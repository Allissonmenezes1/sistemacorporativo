    public void preenche(){ 
         
      
        DefaultTableModel modelo=(DefaultTableModel)tblPag.getModel();
        modelo.setNumRows(0);
        
        DaoPagamento dao=new DaoPagamento();
        
        List<Pagamento>pagr=dao.listPag();
        
        for(Pagamento pag: pagr){
            
            modelo.addRow(new Object[]{
               
                pag.getTipopagamento(),
                pag.getValorrecebido(),
                pag.getTroco(),
                pag.getDatapagamento(),
                pag.getValortotal()});
                    
        }
     }
    

